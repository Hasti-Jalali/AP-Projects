/**
 * This class hold x , y of the disk
 * @author Hasti
 * @version 1.0
 */
public class Disk {
	
	private int x;
	private int y;
	/**
	 * create disk
	 * @param x
	 * @param y
	 */
	public Disk(int x, int y) {
		this.x = x;
		this.y = y;
	}
	/**
	 * get disk's x
	 * @return x
	 */
	public int getX() {
		return x;
	}
	/**
	 * get disk's y
	 * @return y
	 */
	public int getY() {
		return y;
	}
	
}
